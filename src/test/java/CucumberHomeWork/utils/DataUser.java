package CucumberHomeWork.utils;

public class DataUser {
    public String name;
    public String country;
    public String city;
    public String card;
    public String month;
    public String year;

    public DataUser(String name, String country, String city, String card, String month, String year) {
        this.name = name;
        this.country = country;
        this.city = city;
        this.card = card;
        this.month = month;
        this.year = year;
    }
}
